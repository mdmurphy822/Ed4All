"""Wave 76: tests for ``scripts/wave76_clean_concept_graph.py``.

Validates the retroactive cleanup script:

1. Drops nodes whose Wave 76 class is in ``DROPPABLE_CLASSES``.
2. Keeps DomainConcept (and Misconception, by extension).
3. Removes edges whose endpoint(s) refer to dropped nodes.
4. Backs up the original graph before in-place rewrite (``.wave76.bak``).
5. Reports counts before/after, broken down by class.
6. Produces a report whose ``nodes_after`` count equals the kept-class
   total summed.

The script is exercised on a STUB concept graph so the test doesn't
depend on the rdf-shacl-550 archive (which lives under
``LibV2/courses/`` and is regenerated by other Wave 76 workers).
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any, Dict

import pytest

# Make the scripts package importable as a module path. The script
# itself sits at ``scripts/wave76_clean_concept_graph.py`` and works
# fine as a top-level import once ``scripts/`` is on sys.path.
PROJECT_ROOT = Path(__file__).resolve().parents[2]
SCRIPTS_DIR = PROJECT_ROOT / "scripts"
for path in (str(PROJECT_ROOT), str(SCRIPTS_DIR)):
    if path not in sys.path:
        sys.path.insert(0, path)

import wave76_clean_concept_graph as cleaner  # noqa: E402


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _stub_graph() -> Dict[str, Any]:
    """Build a minimal concept graph mixing clean + noisy nodes.

    Mix layout (chosen so every Wave 76 droppable class appears):
    - DomainConcept survivors: ``rdf-graph``, ``blank-node``,
      ``sparql-select``, ``turtle``.
    - PedagogicalMarker drops: ``key-takeaway``, ``rubric``.
    - AssessmentOption drops: ``answer-b``, ``true``.
    - LowSignal drops: ``not``, ``a-literal-is-just``,
      ``pitfall-mdash-target-class``.
    - InstructionalArtifact drops: ``submission-format``.
    - LearningObjective drops: ``to-04``.

    Edges intentionally cross domain ↔ noise boundaries so we can
    verify dangling-endpoint pruning.
    """
    return {
        "kind": "concept",
        "nodes": [
            {"id": "rdf-graph", "label": "RDF Graph", "frequency": 8},
            {"id": "blank-node", "label": "Blank Node", "frequency": 5},
            {"id": "sparql-select", "label": "SPARQL SELECT", "frequency": 4},
            {"id": "turtle", "label": "Turtle", "frequency": 6},
            {"id": "key-takeaway", "label": "Key Takeaway", "frequency": 32},
            {"id": "rubric", "label": "Rubric", "frequency": 12},
            {"id": "answer-b", "label": "Answer B", "frequency": 6},
            {"id": "true", "label": "True", "frequency": 4},
            {"id": "not", "label": "Not", "frequency": 8},
            {"id": "a-literal-is-just", "label": "A Literal Is Just", "frequency": 2},
            {"id": "pitfall-mdash-target-class", "label": "Pitfall Mdash Target Class", "frequency": 3},
            {"id": "submission-format", "label": "Submission Format", "frequency": 5},
            {"id": "to-04", "label": "To 04", "frequency": 7},
        ],
        "edges": [
            # Clean domain↔domain (must survive).
            {"source": "rdf-graph", "target": "blank-node", "weight": 4, "relation_type": "co-occurs"},
            {"source": "rdf-graph", "target": "sparql-select", "weight": 3, "relation_type": "co-occurs"},
            {"source": "turtle", "target": "rdf-graph", "weight": 2, "relation_type": "co-occurs"},
            # Mixed: domain ↔ noisy (must be dropped).
            {"source": "rdf-graph", "target": "key-takeaway", "weight": 5, "relation_type": "co-occurs"},
            {"source": "blank-node", "target": "rubric", "weight": 2, "relation_type": "co-occurs"},
            {"source": "answer-b", "target": "true", "weight": 1, "relation_type": "co-occurs"},
            # Both noisy (must be dropped).
            {"source": "key-takeaway", "target": "rubric", "weight": 4, "relation_type": "co-occurs"},
            {"source": "not", "target": "a-literal-is-just", "weight": 1, "relation_type": "co-occurs"},
            {"source": "submission-format", "target": "to-04", "weight": 3, "relation_type": "co-occurs"},
        ],
        "generated_at": "2026-04-24T00:00:00",
    }


def _write_stub(graph_dir: Path, *, semantic_too: bool = True) -> None:
    graph_dir.mkdir(parents=True, exist_ok=True)
    graph = _stub_graph()
    (graph_dir / "concept_graph.json").write_text(
        json.dumps(graph, indent=2), encoding="utf-8"
    )
    if semantic_too:
        # Reuse the same shape; the script handles both files identically.
        (graph_dir / "concept_graph_semantic.json").write_text(
            json.dumps(graph, indent=2), encoding="utf-8"
        )


