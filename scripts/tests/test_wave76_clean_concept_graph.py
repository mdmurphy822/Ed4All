def test_clean_course_drops_noisy_nodes(tmp_path):
    course_dir = tmp_path / "stub-course"
    _write_stub(course_dir / "graph", semantic_too=False)

    report = cleaner.clean_course(course_dir)

    assert len(report["graphs"]) == 1
    g = report["graphs"][0]

    # 13 nodes in stub; 4 DomainConcept survive.
    assert g["nodes_before"] == 13
    assert g["nodes_after"] == 4

    # Class breakdown: 9 dropped split as
    # PedagogicalMarker=2, AssessmentOption=2, LowSignal=3,
    # InstructionalArtifact=1, LearningObjective=1.
    dropped = g["dropped_class_counts"]
    assert dropped.get("PedagogicalMarker") == 2
    assert dropped.get("AssessmentOption") == 2
    assert dropped.get("LowSignal") == 3
    assert dropped.get("InstructionalArtifact") == 1
    assert dropped.get("LearningObjective") == 1

    # Kept = DomainConcept only.
    assert g["kept_class_counts"] == {"DomainConcept": 4}


def test_clean_course_drops_dangling_edges(tmp_path):
    course_dir = tmp_path / "stub-course"
    _write_stub(course_dir / "graph", semantic_too=False)

    report = cleaner.clean_course(course_dir)
    g = report["graphs"][0]

    # 9 edges in stub, 3 are clean domain↔domain → 6 dropped via
    # dangling endpoint.
    assert g["edges_before"] == 9
    assert g["edges_after"] == 3
    assert g["edges_dropped"] == 6


def test_clean_course_writes_in_place_with_backup(tmp_path):
    course_dir = tmp_path / "stub-course"
    graph_dir = course_dir / "graph"
    _write_stub(graph_dir, semantic_too=False)
    cleaner.clean_course(course_dir)

    target = graph_dir / "concept_graph.json"
    backup = graph_dir / f"concept_graph.json{cleaner.BACKUP_SUFFIX}"

    # Backup created.
    assert backup.is_file()

    # Backup carries the original 13-node payload.
    bak_payload = json.loads(backup.read_text())
    assert len(bak_payload["nodes"]) == 13

    # In-place file shows the cleaned 4-node payload.
    new_payload = json.loads(target.read_text())
    assert len(new_payload["nodes"]) == 4
    new_ids = {n["id"] for n in new_payload["nodes"]}
    assert new_ids == {"rdf-graph", "blank-node", "sparql-select", "turtle"}


def test_clean_course_handles_both_graph_files(tmp_path):
    course_dir = tmp_path / "stub-course"
    _write_stub(course_dir / "graph", semantic_too=True)

    report = cleaner.clean_course(course_dir)

    # Both ``concept_graph.json`` and ``concept_graph_semantic.json``
    # processed.
    assert len(report["graphs"]) == 2
    paths = [g["path"] for g in report["graphs"]]
    assert any(p.endswith("concept_graph.json") for p in paths)
    assert any(p.endswith("concept_graph_semantic.json") for p in paths)


def test_clean_course_dry_run_does_not_write(tmp_path):
    course_dir = tmp_path / "stub-course"
    graph_dir = course_dir / "graph"
    _write_stub(graph_dir, semantic_too=False)

    target = graph_dir / "concept_graph.json"
    backup = graph_dir / f"concept_graph.json{cleaner.BACKUP_SUFFIX}"
    original = target.read_text()

    cleaner.clean_course(course_dir, dry_run=True)

    # No backup created; target unchanged.
    assert not backup.exists()
    assert target.read_text() == original


def test_clean_course_handles_missing_graph_dir(tmp_path):
    """No ``graph/`` subdir → FileNotFoundError, not silent pass."""
    course_dir = tmp_path / "no-graph"
    course_dir.mkdir()
    with pytest.raises(FileNotFoundError):
        cleaner.clean_course(course_dir)


def test_clean_course_skips_missing_files_gracefully(tmp_path, capsys):
    """``concept_graph_semantic.json`` is OPTIONAL; missing → skip
    with a stderr notice, not a crash."""
    course_dir = tmp_path / "stub-course"
    _write_stub(course_dir / "graph", semantic_too=False)
    # Only concept_graph.json exists; concept_graph_semantic.json is
    # absent.
    report = cleaner.clean_course(course_dir)

    err = capsys.readouterr().err
    assert "concept_graph_semantic.json not present" in err
    assert len(report["graphs"]) == 1


def test_clean_course_report_shape(tmp_path):
    """Report shape matches what the CLI ``_print_report`` consumes."""
    course_dir = tmp_path / "stub-course"
    _write_stub(course_dir / "graph", semantic_too=False)
