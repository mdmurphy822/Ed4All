def test_print_report_runs_without_error(tmp_path, capsys):
    course_dir = tmp_path / "stub-course"
    _write_stub(course_dir / "graph", semantic_too=False)
    report = cleaner.clean_course(course_dir, dry_run=True)

    cleaner._print_report(report)
    out = capsys.readouterr().out
