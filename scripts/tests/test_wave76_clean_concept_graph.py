def test_keeps_legitimate_domain_concepts(tmp_path):
    """The script must NEVER drop a DomainConcept-classified node."""
    course_dir = tmp_path / "stub-course"
    graph_dir = course_dir / "graph"
    graph_dir.mkdir(parents=True)
    # All nodes are clean domain vocabulary. Expect zero drops.
    graph = {
        "kind": "concept",
        "nodes": [
            {"id": "rdf-graph", "label": "RDF Graph", "frequency": 5},
            {"id": "owl-2-rl", "label": "OWL 2 RL", "frequency": 3},
            {"id": "shape-constraint", "label": "Shape Constraint", "frequency": 2},
        ],
        "edges": [
            {"source": "rdf-graph", "target": "owl-2-rl", "weight": 2, "relation_type": "co-occurs"},
        ],
        "generated_at": "2026-04-24T00:00:00",
    }
    (graph_dir / "concept_graph.json").write_text(json.dumps(graph, indent=2))

    report = cleaner.clean_course(course_dir)
    g = report["graphs"][0]

    assert g["nodes_before"] == 3
    assert g["nodes_after"] == 3
    assert g["edges_before"] == 1
    assert g["edges_after"] == 1
    assert g["dropped_class_counts"] == {}
